    def _available_actions(self) -> list[str]:
        """
        List of actions the player can currently take.
        Derived from mode + action economy + equipment.
        """
        state = self.game_sm.state
        ctx = state.turn_context

        actions = []

        if state.mode == GameMode.EXPLORING:
            actions = ["move", "look", "interact", "talk", "search", "skill_check"]
        elif state.mode == GameMode.SOCIAL:
            actions = ["talk", "persuade", "intimidate", "insight", "deceive", "leave"]
        elif state.mode == GameMode.TRAVEL:
            actions = ["move", "look", "set_pace", "rest", "scan"]
        elif state.mode == GameMode.COMBAT:
            actions = ["attack", "cast_spell", "dash", "disengage", "dodge", "help",
                       "hide", "ready", "search", "use_object", "shove", "grapple"]
            if ctx.current_actor() == self.character:
                actions += ["end_turn"]

        return actions

