"""
Player Interface — filtered view of game state.

The Player Interface is NOT a separate copy of state.
It is a read/write gateway that:
  - SHOWS players only what their character perceives
  - HIDES: DM notes, hidden secrets, other players' private info, unexplored areas
  - ROUTES all actions through the engine (players never mutate state directly)
  - GATES some information behind skill checks / in-character discovery

Design principle:
  The engine owns truth. The player interface is a lens, not a copy.
  If the player interface needs to "lie" to a player, it does so through
  narrative description generated by the LLM — not by mutating state.
"""

