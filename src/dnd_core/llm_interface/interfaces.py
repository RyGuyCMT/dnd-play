# ─── What the player can see ────────────────────────────────────────────────────

@dataclass
class PlayerViewCharacter:
    """
    The player-character-facing view of a character sheet.
    Omits: DM-only notes, hidden conditions, secret quest flags.
    """
    id: str
    name: str
    hp: int
    hp_max: int
    ac: int
    level: int
    class_name: str
    race: str

    # Visible conditions only
    visible_conditions: list[str] = field(default_factory=list)

    # Exhaustion (visible at all levels per PHB)
    exhaustion_level: int = 0

    # Action economy (current turn)
    movement_remaining: int = 0
    has_action: bool = True
    has_bonus_action: bool = True
    has_reaction: bool = True

    # Resources the player tracks themselves
    spell_slots: dict[int, int] = field(default_factory=dict)  # {level: remaining}
    hit_dice: list[int] = field(default_factory=list)
    death_save_successes: int = 0
    death_save_failures: int = 0

    # Skills the player has (for their own reference)
    skill_bonuses: dict[str, int] = field(default_factory=dict)


@dataclass
class PlayerViewLocation:
    """
    What a player sees about a location.
    Based on visit status, skill checks, and what the LLM has revealed.
    """
    id: str
    name: str
    location_type: str

    # Description: revealed only after visiting / successful perception check
    description: str = ""
    description_revealed: bool = False

    # Connections: visible if explored or asked about
    known_exits: list[str] = field(default_factory=list)  # location names
    exits_revealed: bool = False

    # NPCs: only if met
    npcs_here: list[str] = field(default_factory=list)  # names only, no stats

    # Dangers: only if discovered
    known_dangers: list[str] = field(default_factory=list)


@dataclass
class PlayerViewNPC:
    """
    What a player sees about an NPC.
    Private DM notes and hidden motivations are NEVER exposed here.
    """
    id: str
    name: str
    role: str
    description: str

    # Disposition (as player perceives it)
    disposition: str = "neutral"

    # What the player has learned about this NPC
    known_traits: list[str] = field(default_factory=list)
    conversation_topics: list[str] = field(default_factory=list)  # what player has asked about

    # Quest-related
    quest_flag: Optional[str] = None   # "quest_available", "quest_giver", etc.


@dataclass
class PlayerViewWorld:
    """
    The world as the player perceives it.
    Strictly filtered: unexplored locations are absent, DM notes stripped.
    """
    locations: dict[str, PlayerViewLocation] = field(default_factory=dict)
    visited_location_ids: list[str] = field(default_factory=list)

    # Quests the player knows about
    active_quests: list[str] = field(default_factory=list)   # quest names only


@dataclass
class PlayerViewSession:
    """
    What the player sees about the current session.
    """
    game_date: str = ""
    mode: str = "exploring"
    active_location: Optional[PlayerViewLocation] = None
    active_npc: Optional[PlayerViewNPC] = None

    # Own character
    character: Optional[PlayerViewCharacter] = None

    # Party (other players' visible state)
    party: list[PlayerViewCharacter] = field(default_factory=list)

    # Last LLM narration
    last_narration: str = ""

    # What options the player can take right now
    available_actions: list[str] = field(default_factory=list)


# ─── Player Interface ───────────────────────────────────────────────────────────

class PlayerInterface:
    """
    The player's window into the game.

    Each player gets their own PlayerInterface instance, scoped to their
    character. They can never see:
      - Other players' character sheets (except public info)
      - DM notes, DM secrets, hidden quest objectives
      - Unexplored locations
      - Monster stat blocks they're not meant to see

    All state mutations go through here — the player never calls the engine
    or state machine directly. Instead they call player_* methods which
    route to the appropriate mechanical resolution and return a result.
    """

    def __init__(
        self,
        player_id: str,
        character: CharacterSheet,
        game_sm: GameStateMachine,
        campaign: Campaign,
    ) -> None:
        self.player_id = player_id
        self.character = character          # their own character sheet
        self.game_sm = game_sm
        self.campaign = campaign

        # Active quest knowledge (revealed objectives only)
        self._revealed_objectives: dict[str, list[str]] = {}  # quest_id → [revealed objective ids]

    # ── Read: build player views ─────────────────────────────────────────────

    def view(self) -> PlayerViewSession:
        """Return the full player-visible session state."""
        state = self.game_sm.state

        view = PlayerViewSession(
            game_date=state.turn_context.game_date,
            mode=state.mode.name.lower(),
            character=self._character_view(),
            last_narration=state.turn_context.scene_description,
            available_actions=self._available_actions(),
        )

        # Active location
        if state.turn_context.location_id:
            view.active_location = self._location_view(state.turn_context.location_id)

        # Active NPC
        if state.turn_context.current_npc_id:
            view.active_npc = self._npc_view(state.turn_context.current_npc_id)

        # Party
        for pc_id in self.campaign.player_character_ids:
            if pc_id == self.character.id:
                continue
            pc = self._find_pc(pc_id)
            if pc:
                view.party.append(self._character_view(pc))

        return view

    def _character_view(self, entity: Optional[Entity] = None) -> PlayerViewCharacter:
        """Build player view of a character sheet."""
        if entity is None:
            entity = self.character

        return PlayerViewCharacter(
            id=entity.id,
            name=entity.name,
            hp=entity.hp,
            hp_max=entity.hp_max,
            ac=entity.ac,
            level=getattr(entity, "level", 1),
            class_name=getattr(entity, "class_name", "Unknown"),
            race=getattr(entity, "race", "Unknown"),
            visible_conditions=[c.condition.name for c in entity.conditions],
            exhaustion_level=entity.exhaustion,
            movement_remaining=entity.economy.movement,
            has_action=entity.economy.action,
            has_bonus_action=entity.economy.bonus_action,
            has_reaction=entity.economy.reaction,
            spell_slots={i: entity.spell_slots.slot_max(i) for i in range(1, 10)},
            death_save_successes=entity.death_saves.successes,
            death_save_failures=entity.death_saves.failures,
            skill_bonuses={},  # TODO: fill from character class
        )

    def _location_view(self, location_id: str) -> Optional[PlayerViewLocation]:
        """Build player view of a location (respects exploration status)."""
        if self.campaign.world is None:
            return None

        loc = self.campaign.world.locations.get(location_id)
        if loc is None:
            return None

        revealed = loc.visit_status.value >= 2  # EXPLORED or HOME_BASE

        return PlayerViewLocation(
            id=loc.id,
            name=loc.name,
            location_type=loc.location_type.name.lower(),
            description=loc.description if revealed else "",
            description_revealed=revealed,
            known_exits=[],  # TODO: reveal connections after exploration
            exits_revealed=revealed,
            npcs_here=[],     # TODO: reveal NPCs after meeting them
            known_dangers=[], # TODO: reveal after perception / discovery
        )

    def _npc_view(self, npc_id: str) -> Optional[PlayerViewNPC]:
        """Build player view of an NPC (no DM notes ever)."""
        if self.campaign.world is None:
            return None

