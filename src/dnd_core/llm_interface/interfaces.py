class DMInterface:
    """
    The DM's window into the game. Sees everything, can modify everything.
    """

    def __init__(
        self,
        game_sm: GameStateMachine,
        campaign: Campaign,
        dm_notes: "DMNotes",
    ) -> None:
        self.game_sm = game_sm
        self.campaign = campaign
        self.dm_notes = dm_notes

    def full_state(self) -> SessionState:
        """Return the complete unfiltered session state."""
        return self.game_sm.state

    def world(self) -> WorldState:
        """Return the full world state."""
        return self.campaign.world or WorldState(world_id="", name="Empty World")

    # ── World building ───────────────────────────────────────────────────

    def create_location(self, location: Location) -> None:
        if self.campaign.world is None:
            self.campaign.world = WorldState(world_id=self.campaign.id, name=self.campaign.name)
        self.campaign.world.add_location(location)

