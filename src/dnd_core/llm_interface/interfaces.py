    def _find_combat_target(self, name: str) -> Optional[Entity]:
        """Find a combatant by name in the current encounter."""
        state = self.game_sm.state
        if not state.is_combat():
            return None
        for entry in state.initiative_order:
            if entry.entity.name.lower() == name.lower():
                return entry.entity
        return None

